# Ввод числа N
N = int(input("Введите целое число N (1 < N < 26): "))

# Проверка условия
if 1 < N < 26:
    # Получаем последние N букв латинского алфавита в обратном порядке
    last_letters = [chr(i) for i in range(ord('z'), ord('z') - N, -1)]
    print("Последние N строчных букв латинского алфавита в обратном порядке:", ''.join(last_letters))
else:
    print("Число N должно быть в диапазоне (1, 26).")