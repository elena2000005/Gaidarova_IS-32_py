# Ввод размера списка
N = int(input("Введите размер списка N: "))
A = [int(input(f"Введите элемент {i+1}: ")) for i in range(N)]

# Ввод индексов K и L
K = int(input("Введите K (1 < K < L < N): ")) - 1  # Индексы с 0
L = int(input("Введите L (1 < K < L < N): ")) - 1

# Перестановка элементов в обратном порядке
A[K:L+1] = A[K:L+1][::-1]

# Вывод результата
print("Измененный список:", A)