# Функция для нахождения количества цифр и их суммы
def DigitCountSum(K):
    C = 0  # количество цифр
    S = 0  # сумма цифр
    while K > 0:
        digit = K % 10  # последняя цифра
        S += digit     # добавляем цифру к сумме
        C += 1         # увеличиваем количество цифр
        K //= 10       # удаляем последнюю цифру
    return C, S

# Обработка пяти чисел
for i in range(5):
    K = int(input(f"Введите целое число {i+1}: "))
    C, S = DigitCountSum(K)
    print(f"Число {K}: количество цифр = {C}, сумма цифр = {S}")