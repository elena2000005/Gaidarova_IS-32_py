# Ввод данных
V1 = float(input("Введите скорость первого автомобиля (км/ч): "))
V2 = float(input("Введите скорость второго автомобиля (км/ч): "))
S = float(input("Введите начальное расстояние между автомобилями (км): "))
T = float(input("Введите время движения (ч): "))

# Суммарная скорость
total_speed = V1 + V2

# Общий путь, пройденный автомобилями
total_distance = total_speed * T

# Расстояние между автомобилями через T часов
distance = abs(S - total_distance)

# Вывод результата
print(f"Расстояние между автомобилями через {T} часов: {distance} км")
