# Создаем словарь с книжными магазинами и их коллекциями
bookstores = {
    "Магистр": {"Лермонтов", "Достоевский", "Пушкин", "Тютчев"},
    "ДомКниги": {"Толстой", "Грибоедов", "Чехов", "Пушкин"},
    "БукМаркет": {"Пушкин", "Достоевский", "Маяковский"},
    "Галерея": {"Чехов", "Тютчев", "Пушкин"}
}

# Книги, которые нужно проверить
search_authors = {"Пушкин", "Тютчев"}

# Список магазинов, где можно найти искомых авторов
available_stores = []

# Проверка наличия книг в магазинах
for store, authors in bookstores.items():
    if search_authors.issubset(authors):
        available_stores.append(store)

# Вывод результата
print(f"Магазины, в которых можно приобрести книги Пушкина и Тютчева: {', '.join(available_stores)}")